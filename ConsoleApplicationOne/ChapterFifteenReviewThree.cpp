#include <stdio.h>
int main(void)
{
	printf("~3  = :  %d\n", (~3));
	printf("3 & 6 = :  %d\n", (3 & 6));
	printf("3 | 6 = :  %d\n", (3 | 6));
	printf("1 | 6 = :  %d\n", (1 | 6));
	printf("3 ^ 6 = :  %d\n", (3 ^ 6));
	printf("7 >> 1 = :  %d\n", (7 >> 1));
	printf("7 << 2 = :  %d\n", (7 << 2));
	return 0;
}